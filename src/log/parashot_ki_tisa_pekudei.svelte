<h3>ki tisa</h3>
<h4>counting</h4>
<p>right in the beginning of the portion we get:</p>
<blockquote class="parentblock">
	<cite
		><RainbowLink href="https://www.sefaria.org/Exodus.30.12?lang=bi&aliyot=0"
			>Exodus 30:12-13</RainbowLink
		></cite
	>
	<Hebrew>
		כִּ֣י תִשָּׂ֞א אֶת־רֹ֥אשׁ בְּנֵֽי־יִשְׂרָאֵל֮ לִפְקֻדֵיהֶם֒ וְנָ֨תְנ֜וּ אִ֣ישׁ כֹּ֧פֶר נַפְשׁ֛וֹ
		לַיי בִּפְקֹ֣ד אֹתָ֑ם וְלֹא־יִהְיֶ֥ה בָהֶ֛ם נֶ֖גֶף בִּפְקֹ֥ד אֹתָֽם׃ (יג) זֶ֣ה ׀ יִתְּנ֗וּ
		כׇּל־הָעֹבֵר֙ עַל־הַפְּקֻדִ֔ים מַחֲצִ֥ית הַשֶּׁ֖קֶל בְּשֶׁ֣קֶל הַקֹּ֑דֶשׁ עֶשְׂרִ֤ים גֵּרָה֙
		הַשֶּׁ֔קֶל מַחֲצִ֣ית הַשֶּׁ֔קֶל תְּרוּמָ֖ה לַֽיי׃
	</Hebrew>
	<Translation>
		(12) When you take a census of the Israelite men according to their army enrollment, each shall
		pay יי a ransom for himself on being enrolled, that no plague may come upon them through their
		being enrolled. (13) This is what everyone who is entered in the records shall pay: a
		half-shekel by the sanctuary weight—twenty gerahs to the shekel—a half-shekel as an offering to
		יי.
	</Translation>
</blockquote>
<p>
	this was one of the first sections of torah that really made me go <b>!!!!!</b> when reading the alter
	translation a couple years ago. alter's footnote here is:
</p>
<blockquote>
	It was a belief common to Israel and to the Mesopotamian cultures that it was dangerous for humans
	to be counted. Perhaps it was felt that assigning individuals in a mass an exact number set them
	up as vulnerable targets for malefic forces. The story of David’s ill-fated census in 2 Samuel 24,
	which triggers a plague, turns on this belief. The danger of destruction inherent in census taking
	could be averted by the payment of a “ransom” for each threatened life as a donation to the
	sanctuary. The supposed danger of the census thus becomes the rationale for the institution of a
	poll tax, which in turn will be an important source of revenue for the maintenance of the
	sanctuary and its officiants.
</blockquote>
<p>
	Forget 'belief common to Israel and to the Mesopotamian cultures' and 'malefic forces!' The
	association between census and taxation/the draft is probably common to most if not all people
	living under some form of centralized governance, because it's just straight up how that works. if
	someone comes around asking 'hey how many able-bodied males live here, would you say?' you can
	safely assume they're coming for taxes or to conscript. I think it's pretty incredible that the
	argument for "let yourselves be counted this time, please," is the exact argument that's made
	every 10 years when the US federal census is taken, which is "because it will also help fund
	public works!" In this case it's the subsequent building and maintenance of the mishkan, in the
	contemporary US it's the dangling carrot of "maybe we'll fund public schools this time haha just
	kidding," but the logic is the same.
</p>
<p>The traditional justifications of why we don't count jews look at:</p>
<ul>
	<li>
		this section of exodus, where counting shekels rather than Israelites directly is laid out as a
		way to ward off plague;
	</li>
	<li>
		the section of Samuel which Alter brings up, in which the direct counting of Jews leads to a
		plague;
	</li>
	<li>
		and <RainbowLink href="https://www.sefaria.org/Hosea.2.1?lang=bi&with=all&lang2=en"
			>Hosea 2:1</RainbowLink
		> in which it literally says "the number of the people of Israel . . . cannot be measured or counted."
	</li>
</ul>

<p>
	Rashi argues that it's to avoid the evil eye, Brachot 62b:11<SuperScript id="hello" /> posits that
	David would have had to "incite" Gd into making him forget such a basic matter "as even schoolchildren
	know" as not to count Jews directly, both of which give the ban the feel of a pre-existing folk tradition
	merely reinforced/recorded by textual sources. The prohibition on directly counting jews under any
	circumstance (say, to determine if there's a minyan) is, as far as I know, distinctly-if-probably-not-uniquely
	Jewish. but I thought it was really cool and ironic that this prohibition might have come out of a more
	generalized distrust of attempts to centralize power.
</p>
<h4>modern echoes</h4>
<p>
	i was reading the Summer 1973 issue of <i>Response: A Contemporary Jewish Journal</i>, which I
	picked up on the street a few weeks ago, which just so happens to be titled "THE JEWISH WOMAN: An
	Anthology" and is an absolute head-of-the-second-wave production start to finish. We have an essay
	by Judith Plaskow to start us off (hi Judith! fancy seeing you here Judith!) and then launch into
	a bunch of quite bad essays and a couple random gems by people I'd never heard of and couldn't
	find anything about on Google. One of these is "What Made Yetta Work? The Economic Role of Eastern
	European Jewish Women in the Family" by Charlotte Baum, which is somewhat an examination of, but
	largely a takedown of others' unsatisfactory answers to, the titular question. In her examination
	of other scholars' misrepresentation of the centrality of Jewish women in the economic survival of
	Eastern European Jewry, Baum discusses the undercounting of women in the 1987 Russian Census:
</p>
<blockquote>
	<b>p.34</b>
	<p>
		It is possible, however, that the Jewish community aided in the underestimation [of womens'
		participation in the workforce]. Lloyd P. Gartner points out that Russian Jews long associated
		inquiries with extortionate taxation and conscription; one suspects, therefore, that Jews might
		not have reported womens' employment honestly.
	</p>
</blockquote>
<p>
	As in ancient Mesopotamia, so in imperial Russia. The distrust for counting here interestingly
	seems to doubly impact women --- first in the census itself, wherein Baum seems to assume it was
	easier to conceal womens' economic participation than mens', and in subsequent historical writing,
	which takes this omission blindly and adds an entirely faulty assumption that "the proverbial
	sanctity of the Jewish home" kept women out of industrial work. It's a double bind familiar to any
	would-be historian of activity which is often kept out of the official record: on the one hand,
	you don't want people to get caught (evading census and taxation/conscription, sodomizing,
	crossdressing, etc). but on the other hand, we often only get documentation of these delightful
	activities when people do get caught.
</p>

<h3>pekudei</h3>
<p>
	the last section of exodus is concerned with a different kind of census , an inventory of all the
	objects the Israelites have assembled and constructed for ritual purposes. the word pekudei here
	(פְקוּדֵ֤י) is actually the literal same word used for the census in ki tisa. pekudei is, on first
	glance, a strange way to wrap up the slave revolt epic of Exodus. Usually I gloss over sections
	like this with the usual "priestly writers and their special interests" nod, but I was struck by
	the fact that actually, this is a really poignant way to end the narrative of liberation from
	slavery. The Israelites <i>are</i> property, Pharaoh's property, in the beginning of Exodus. This accounting
	of their material possessions bookends that with a sense of accomplishment and deliverance, laying
	out the necessity and dignity of sovereignty over the creation and maintenance of the materials which
	enable spiritual and communal life. It feels like a bittersweet, incomplete end to the liberation story
	--- it's hard for me to see the lavish attention to the material details of the "cult" outside its
	being a clear display of wealth and power by the priestly writers. And of course in the literal text
	itself, the rest of the Torah involves the Israelites wandering around in the desert, not immediately
	settling down into luxury and ease.
</p>
<p>
	But I also like this tension, between the undertaking of a census of people threatening to bring
	plague on the one hand; and a census of holy property enacting material liberation from having the
	legal/social status of <i>being</i> property on the other. Reading as I am after the establishment
	of the state of Israel (and before its demise), the more military and "national"-oriented sections
	of Torah often feel frictional. I feel cautious about the metaphor of material wealth as a symbol of
	liberation from suffering, even if it is textually material wealth for Gd's glory and not for humankind's.
	But the Exodus narrative is also not about total redemption of the world into olam haba, it is a story
	of the liberation and consecration of a people unto Hashem. Again, I think we see similar struggles
	between a full liberation and a liberation into a different, perhaps lesser form of subjugation play
	out in many national liberation struggles.
</p>
